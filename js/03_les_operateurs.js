// L'addition

// 1- Déclaration et affectation de plusieurs variables :

var nb1, nb2, resultat;

nb1 = 10;
nb2 = 5;

// L'addition de nb1 et nb2 avec l'opérateur "+"
resultat = nb1 + nb2;
console.log(resultat);

// La soustraction de nb1 et nb2 avec l'opérateur "-"
resultat = nb1 - nb2;
console.log(resultat);

// La div de nb1 et nb2 avec l'opérateur "/"
resultat = nb1 / nb2;
console.log(resultat);

// La * de nb1 et nb2 avec l'opérateur "*"
resultat = nb1 * nb2;
console.log(resultat);

/* 
Le modulo corresp au reste de la division.
Le modulo de nb1 et nb2 avec l'opérateur "%"
*/
resultat = nb1 % nb2;
console.log(resultat);

