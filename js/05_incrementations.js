/* ----------------------------------
    INCREMENTATION ET DÉCRÉMENTATION
 -----------------------------------*/

// Incrémentation

var nb1 = 1;
nb1 = nb1 + 1;
nb1 +=1;
// Écriture simplifiée
nb1++;
console.log(nb1);

// Décrémentation
var nb2 = 1;
nb2 = nb1-1;
nb2 -= 1;
// Écriture simplifiée
nb2--;
console.log(nb2);

// Subtilité
nb1++; /* a le même résultat que */ ++nb1;
